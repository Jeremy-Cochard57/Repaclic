    <!-- Section 1-->
    <section id="section1" class="section">
        <h2 class="section-title">1. Protégez vos données, protégez vos proches !</h2>
        <p class="paragraph">La cybersécurité joue un rôle crucial dans la <strong>protection de vos informations
                sensibles.</strong> <br>Dans un monde de plus en plus connecté, les données personnelles, financières et
            professionnelles sont souvent ciblées par des cybercriminels.Sans une cybersécurité adéquate, ces
            informations peuvent être <strong>volées</strong>, et être en possession de personnes malintentionnées, qui
            peuvent être utilisées pour commettre des <strong>fraudes ou usurper votre identité.</strong><br>Protéger
            vos données, c’est non seulement <strong>préserver votre vie privée</strong>, mais aussi <strong>protéger
                ceux qui vous entourent.</strong></p>
    </section>
    <!-- Section 2-->
    <section id="section2" class="section">
        <h2 class="section-title">2. Sauvegardez vos données personnels dans le cloud !</h2>
        <p class="paragraph">La sauvegarde des données est essentielle pour <strong>protéger vos informations contre les
                pertes</strong>, que ce soit à cause de cyberattaques, de pannes matérielles ou d’erreurs humaines.
            Cependant, pour maximiser la sécurité de vos données, il ne suffit pas de tout stocker au même
            endroit.<br>Tout comme en bourse, <strong>il est risqué de mettre tous vos œufs dans le même
                panier.</strong> Si vous placez toutes vos données dans un seul espace de stockage, un incident, qu'il
            soit technique ou malveillant, <strong>pourrait entraîner leur perte totale.</strong><br>C’est pourquoi il
            est crucial de <strong>diversifier vos options de sauvegarde</strong> : combinez des solutions externes
            (disques durs, serveurs) et cloud (stockage en ligne) pour limiter les risques et garantir une protection
            optimale.</p>
    </section>
    <!-- Section 3-->
    <section id="section3" class="section">
        <h2 class="section-title">3. Vérifiez les sites avant de faire des achats !</h2>
        <p class="paragraph">Si le commerce en ligne facilite les achats et offre l’opportunité de faire de bonnes
            affaires, il existe malheureusement de nombreux sites de vente douteux, voire malveillants.<br>Avant
            d’acheter sur Internet, vérifiez que vous n’êtes pas sur une copie frauduleuse d’un <strong>site
                officiel</strong>, la crédibilité de l’offre et consultez les avis. <br>Si elle commence par «
            <strong>https</strong> » au lieu de « http », cela signifie que vous êtes sur un site sécurisé par le
            certificat SSL (le « s » signifie sécurisé). L'icône d’un <strong>cadenas</strong> apparaît souvent dans la
            barre supérieure de votre navigateur ou dans le coin inférieur droit des pages des sites de paiement
            sécurisés. <br>Sans cette vérification, vous prenez le risque de vous faire <strong>dérober votre numéro de
                carte bancaire</strong> et de ne jamais recevoir votre commande, voire de recevoir une contrefaçon ou un
            produit dangereux.
        </p>
    </section>
    <!-- Section 4-->
    <section id="section4" class="section">
        <h2 class="section-title">4. Anticipez avant d’être ciblé !</h2>
        <p class="paragraph">L’usurpation d’identité est un danger réel. Lorsque des cybercriminels accèdent à vos
            informations personnelles, ils peuvent les utiliser pour commettre des actes illégaux, comme réaliser des
            achats frauduleux ou effectuer des transactions bancaires non autorisées.<br>Ils peuvent aussi
            <strong>exploiter votre matériel</strong> pour mener des activités criminelles.Ce qui est encore plus
            préoccupant, <strong>c'est que vous serez tenu responsable de ces actes.</strong> Les autorités n'hésiteront
            pas à vous impliquer dans des enquêtes, et vous devrez prouver que vous n’êtes pas à l’origine des
            infractions commises, ce qui peut causer de <strong>graves préjudices</strong> à votre réputation et à votre
            vie personnelle.<br>Protéger vos informations, c’est <strong>éviter que des criminels ne détournent votre
                identité</strong> et utilisent vos ressources pour des actes illégaux.
        </p>
    </section>
    <!-- Section 5-->
    <section id="section5" class="section">
        <h2 class="section-title">5. Évitez les réseaux WiFi publics ou inconnus !</h2>
        <p class="paragraph">En mobilité, privilégiez la <strong>connexion de votre abonnement téléphonique </strong>(3G
            ou 4G) aux réseaux WiFi publics. <br>Ces réseaux WiFi sont souvent mal sécurisés, et <strong>peuvent être
                contrôlés ou usurpés par des pirates</strong> qui pourraient ainsi voir passer et <strong>capturer vos
                informations personnelles ou confidentielles (mots de passe, numéro de carte bancaire…).</strong><br>Si
            vous n’avez d’autre choix que d’utiliser un WiFi public, veillez à ne jamais y réaliser d’opérations
            sensibles et <strong>utilisez si possible un réseau privé virtuel (VPN).</strong></p>
    </section>
    <!-- Section 6-->
    <section id="section6" class="section">
        <h2 class="section-title">6. Séparez vos usages personnels et professionnels !</h2>
        <p class="paragraph">Avec l’accroissement des usages numériques, la frontière entre utilisation personnelle et
            professionnelle est souvent ténue. Ces utilisations peuvent même parfois s’imbriquer. Matériels,
            messageries, « clouds »… Il est important de séparer vos usages afin que le piratage d’un accès personnel ne
            puisse pas nuire à votre entreprise, ou inversement, que la compromission de votre entreprise ne puisse pas
            avoir d’impact sur la sécurité de vos données personnelles.</p>
    </section>
    <!-- Section 7-->
    <section id="section7" class="section">
        <h2 class="section-title">7. Investissez dans des soltions de sécurité !</h2>
        <p class="paragraph">Les <strong>antivirus permettent de se protéger d’une grande majorité d’attaques et de
                virus connus</strong>.<br>Il existe de nombreuses solutions gratuites ou payantes selon vos usages et le
            niveau de protection ou de services recherchés.<br><strong>Vérifiez régulièrement que les antivirus de vos
                équipements sont bien à jour</strong> et <strong>faites des analyses</strong> (scans) approfondies pour
            vérifier que vous n’avez pas été infecté.</p>
    </section>
    <!-- Section 8-->
    <section id="section8" class="section">
        <h2 class="section-title">8. Prenez le temps de faire les mises à jours !</h2>
        <p class="paragraph"><strong>Les fabricants publient régulièrement</strong> des <strong>correctifs pour combler
                des failles de sécurité</strong> qui pourraient être exploitées par des cybercriminels. En négligeant
            ces mises à jour, vous exposez vos données et votre système à des risques d’attaques. <br>Des pirates
            peuvent s’introduire dans vos appareils, pour y <strong>dérober vos informations personnelles</strong>,
            voire pour <strong>détruire vos données</strong> ou encore vous <strong>espionner.</strong></p>
    </section>
    <!-- Section 9-->
    <section id="section9" class="section">
        <h2 class="section-title">9. Méfiez vous des messages inattendus !</h2>
        <p class="paragraph">En cas de réception d’un message inattendu ou alarmiste par messagerie (email), SMS ou
            chat, <strong>demandez toujours confirmation à l’émetteur par un autre moyen</strong> s’il vous semble connu
            et légitime. Il peut en effet s’agir d’une attaque par hameçonnage (phishing) visant à vous piéger pour vous
            dérober des informations confidentielles, de l’envoi d’un <strong>virus contenu dans une
                pièce-jointe</strong> qu’on vous <strong>incite à ouvrir</strong>, ou d’un <strong>lien qui vous
                attirerait sur un site malveillant.</strong></p>
    </section>
    <!-- Section 10-->
    <section id="section10" class="section">
        <h2 class="section-title">10. Communiquez à vos proches !</h2>
        <p class="paragraph"><strong>La cybersécurité est l'affaire de tous !</strong>En partageant les bonnes pratiques
            avec votre entourage, <strong>vous contribuez à renforcer la protection collective contre les
                cybermenaces</strong>. Informer ses proches sur l'importance des mises à jour, des mots de passe
            sécurisés et de la vigilance en ligne permet de limiter les risques d'attaques. Plus nous adoptons ensemble
            ces réflexes, plus nous <strong>compliquons la tâche des cybercriminels.</strong><br><strong>Sensibiliser,
                c'est agir pour un internet plus sûr pour tous !</strong></p>
    </section>
